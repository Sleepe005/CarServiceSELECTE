CREATE TABLE cars
(
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    brand               VARCHAR(255)                            NOT NULL,
    model               VARCHAR(255)                            NOT NULL,
    generation          VARCHAR(255)                            NOT NULL,
    year_from           date                                    NOT NULL,
    year_to             date                                    NOT NULL,
    price               BIGINT                                  NOT NULL,
    safety_rating       BIGINT                                  NOT NULL,
    reliability_rating  BIGINT                                  NOT NULL,
    fuel_consumption    BIGINT                                  NOT NULL,
    maintenance_cost    BIGINT                                  NOT NULL,
    comfort_rating      BIGINT                                  NOT NULL,
    capacity_passengers BIGINT                                  NOT NULL,
    capacity_baggage    BIGINT                                  NOT NULL,
    acceleration_0_100  BIGINT                                  NOT NULL,
    appearance_rating   BIGINT                                  NOT NULL,
    body_type           VARCHAR(255)                            NOT NULL,
    fuel_type           VARCHAR(255)                            NOT NULL,
    transmission        VARCHAR(255)                            NOT NULL,
    drivetrain          VARCHAR(255)                            NOT NULL,
    power_hp            BIGINT                                  NOT NULL,
    additional_options  VARCHAR(255)                            NOT NULL,
    last_updated        date                                    NOT NULL,
    CONSTRAINT pk_cars PRIMARY KEY (id)
);